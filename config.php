        <textarea name="config_content" id="lua-editor" class="lua-editor" spellcheck="false"><?= htmlspecialchars($configContent) ?></textarea>
      </div>
      <div class="editor-actions">
        <button type="submit" class="btn-primary">💾 SAVE CONFIG.LUA</button>
        <span class="editor-hint">Ctrl+S to save · Lua syntax</span>
